type Leaf = Record<string, string | Leaf>;

function pick(dict: Leaf, path: string): string | undefined {
  const parts = path.split(".");
  let cur: unknown = dict;
  for (const p of parts) {
    if (cur && typeof cur === "object" && p in (cur as Leaf)) cur = (cur as Leaf)[p];
    else return undefined;
  }
  return typeof cur === "string" ? cur : undefined;
}

